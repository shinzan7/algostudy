package ja.lc;

import java.util.Scanner;

public class 입력_자가진단7_515 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		int a = sc.nextInt();
		int b = sc.nextInt();
		
		System.out.printf("%d * %d = %d\n", a, b, a*b);
		System.out.printf("%d / %d = %d", a, b, a/b);
	}

}
